package controlFlow;

public class ComparisonOperators {
    public static void main (String[] args) {
//        comparison operators  - to compare primitive types
        int x = 1;
        int y = 2;

        System.out.println(x == y);     // equality operator
        System.out.println(x != y);    //  inequality operator
        System.out.println(x <= y);   //   less or equal to
        System.out.println(x >= y);  //    greater than or equal to
    }

}
